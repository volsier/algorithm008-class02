// @lc code=start
func climbStairs(n int) int {
    if n == 0 || n == 1 || n ==2 || n== 3{
        return n
    }
    r := make([]int,n)
    r[0] = 1
    r[1] = 2
    for i := 2;i<n;i++{
        r[i] = r[i-1] +r[i-2]
    }
    return r[n-1]
}
